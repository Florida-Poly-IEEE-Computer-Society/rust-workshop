fn main() {
    let x = 6;
    let y = 5;

    let mut z = x + y; // adding
    println!("{z}");

    z = x - y; // subtraction
    println!("{z}");

    z = x * y; // multiplication
    println!("{z}");

    z = x / y; // What happens when we divide integers?
    println!("{z}"); // we get an integer

    z = x % y; // module or remainder division
    println!("{z}");
}

